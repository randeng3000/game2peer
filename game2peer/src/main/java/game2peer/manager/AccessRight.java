package game2peer.manager;

public class AccessRight {

	private int get;
	private int del;
	private int put;
	private int modi;
	
	public int getGet() {
		return get;
	}
	public void setGet(int get) {
		this.get = get;
	}
	public int getDel() {
		return del;
	}
	public void setDel(int del) {
		this.del = del;
	}
	public int getPut() {
		return put;
	}
	public void setPut(int put) {
		this.put = put;
	}
	public int getModi() {
		return modi;
	}
	public void setModi(int modi) {
		this.modi = modi;
	}
}
